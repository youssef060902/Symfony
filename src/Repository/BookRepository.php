    public function findBooksByAuthorId(int $id): array
    {
        
        $entityManager = $this->getEntityManager();

        $query = $entityManager->createQuery(
            'SELECT b 
            FROM App\Entity\Book b
            JOIN b.author a
            WHERE a.id = :authorId
            ORDER BY a.name ASC'
        )->setParameter('authorId', $id);

        
        return $query->getResult();
    }
